#!/usr/bin/env ruby

def ok cmd, expect
    actual = %x[./ef hex #{cmd}]
    actual.chomp!

    raise "Command './ef hex #{cmd}' exitted with #{$?.to_i}, expected 0" if $?.to_i != 0

    if actual != expect
        puts "Expected: #{expect}"
        puts "Actual:   #{actual}"
        raise "Wrong output"
    end

    %x[./ef pcap ./out.pcap #{cmd}]
    raise "Command './ef pcap #{cmd}' exitted with #{$?.to_i}, expected 0" if $?.to_i != 0

    puts "OK: #{cmd}"

    #%x[valgrind ./ef hex #{cmd}]
    #raise "Command './ef hex #{cmd}' exitted with #{$?.to_i}, expected 0" if $?.to_i != 0
end

